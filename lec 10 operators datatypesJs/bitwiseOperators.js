// bitwisw operators 04 lecture sanket js

// 1. bitwise AND -> &
const a = 5; // 101
const b = 6; // 1010

console.log(a & b); // 4

// 2. bitwise OR -> |
const c = 4; // 100
const d = 3; // 011
console.log(c | d) // 

// 3. bitwise XOR -> ^
const e = 4; // 100
const f = 3; // 011
console.log(e ^ f) // 7
console.log(6 ^ 7) // 1

// 4. bitwise NOT -> ~
console.log(~5) // -6
console.log(~-5) // 4

// 5. left shift -> <<
console.log(5 << 1) // 10
console.log(8 << 3) // 20
console.log(1 << 4) // 16
console.log(1 << 5) // 32

// 6. right shift ->  >>
console.log(5 >> 1) // 2
console.log(27 >> 2) // 6
console.log(9 >> 1) // 4
console.log(9 >> 2) // 2
