// operators in javascript

// 1. Arithmetic operators
let a = 10;
let b = 3;
console.log("a + b = ", a + b);
console.log("a - b = ", a - b);
console.log("a * b = ", a * b);
console.log("a / b = ", a / b);
console.log("a % b = ", a % b);
console.log("a ** b = ", a ** b);
// 2. Increment (++) and Decrement (--) operators
let c = 5;
console.log("c = ", c);
console.log("c++ = ", c++);

let d = 3;
console.log("d = ", d);
console.log("++d = ", ++d);

let e = 4;
console.log("e = ", e);
console.log("e-- = ", e--);

let f = 2;
console.log("f = ", f);
console.log("--f = ", --f);



