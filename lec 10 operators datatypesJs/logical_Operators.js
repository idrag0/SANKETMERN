// Logical operators in javascript

// 1. Logical AND (&&) operator
let a = true;
let b = false;
let c = true;
console.log("a && b = ", a && b);
console.log("a && c = ", a && c);

// 2. Logical OR (||) operator
let e = true;
let d = false;
let f = false;

console.log("c || d = ", c || d);
console.log("d || f = ", d || f);
console.log("e || f = ", e || f);

// 3. Logical NOT (!) operator
let g = true;
let h = false;
console.log("!g = ", !g);
console.log("!h = ", !h);
